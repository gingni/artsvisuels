<h2 style='margin-left:0cm'><a name="_Toc381289879"><span style='font-variant:
normal !important;text-transform:uppercase'>Supprimer une image d’une galerie</span></a></h2>

</div>

<h3>Objectif</h3>

<p class=MsoNormal style='line-height:normal'>Supprimer l’image d’une ou des
galeries qui la contienne</p>

<h3>Description</h3>

<p class=MsoNormal style='line-height:normal'>À la suite du cas «&nbsp;supprimer
une image&nbsp;», l’application web supprime l’image de toutes les galeries qui
la contiennent.</p>

<p class=MsoNormal style='line-height:normal'>Aussi, le membre accède à la
maintenance du site et demande à modifier le contenu d’une galerie,
l’application affiche une liste des images qu’elle contient.  L’utilisateur
peut choisir une image puis cliquer sur le bouton supprimer.  L’image n’est pas
supprimée mais n’appartient plus à cette galerie.</p>

<div style='border:none;border-bottom:solid windowtext 1.0pt;padding:0cm 0cm 1.0pt 0cm;
margin-left:14.2pt;margin-right:0cm'>

<h2 style='margin-left:0cm'><a name="_Toc381289880"><span style='font-variant:
normal !important;text-transform:uppercase'>Rechercher une image</span></a></h2>

</div>

<h3>Objectif</h3>

<p class=MsoNormal style='line-height:normal'>Rechercher une ou les images
d'une galerie, d’un étudiant, par mot clé ou autre critère</p>

<h3>Description</h3>

<p class=MsoNormal style='line-height:normal'>Le membre accède à la maintenance
du site et demande une recherche d’images, il peut choisir de faire la
recherche par galerie, par membre, par mot clé ou autre critère utile. 
L’application affiche les images.</p>

<p class=MsoNormal style='line-height:normal'>Aussi, lorsqu’un visiteur clique
sur le lien d’une page, sur la partie publique du site, l’application web
recherche et affiche les images de cette page.</p>

<div style='border:none;border-bottom:solid windowtext 1.0pt;padding:0cm 0cm 1.0pt 0cm;

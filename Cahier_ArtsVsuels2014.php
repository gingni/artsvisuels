<h1><a name="_Toc381289871">But de l’application web</a></h1>

<p class=MsoNormal style='line-height:normal'><a name="_Toc366588102"></a>L’application
web est constituée de deux parties&nbsp;:</p>

<p class=MsoNormal style='line-height:normal'>Une partie privée qui permet de
gérer les images des travaux des étudiants du département d’arts plastiques. 
Les étudiants ajoutent les photos de leurs réalisations pour les travaux
sélectionnés par les professeurs.  Les professeurs peuvent demander que chaque
étudiant commente les œuvres des autres étudiants.   Les professeurs peuvent
ainsi, à partir de la banque d’images crée par les étudiants, sélectionner les
œuvres à publier sur le site Web.</p>

<p class=MsoNormal style='line-height:normal'>Une partie publique qui permet à
tous de visualiser les œuvres publiées.</p>

<h1><a name="_Toc381289872">Glossaire</a></h1>

<p class=MsoNormal style='line-height:normal'><em><span style='font-family:
"Calibri","sans-serif"'>Partie privée</span></em>&nbsp;: Section du site web
qui ne peut être accédée sans s’être authentifié.  Cette partie est réservée à
la maintenance du site –gestion des membres, galeries, groupes— et à l’ajout
d’images, commentaires et autres.  Naturellement, les membres étudiants n’ont
pas accès aux mêmes fonctionnalités que les membres professeurs.  De même le
professeur qui s’authentifie comme gestionnaire a accès à des tâches bien
précises.</p>

<p class=MsoNormal style='line-height:normal'><em><span style='font-family:
"Calibri","sans-serif"'>Partie publique</span></em>&nbsp;: Section du site web
qui est visible par toute personne naviguant sur le web.</p>


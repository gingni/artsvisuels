<h2 style='margin-left:0cm'><a name="_Toc381289881"><span style='font-variant:
normal !important;text-transform:uppercase'>Ajouter une galerie</span></a></h2>

</div>

<h3>Objectif</h3>

<p class=MsoNormal style='line-height:normal'>Dans le cadre d’un cours, ajouter
une galerie pour contenir les images relatives à un travail. Ces images sont
visibles seulement dans la partie privée du site.</p>

<h3>Description</h3>

<p class=MsoNormal style='line-height:normal'>Le professeur accède à la
maintenance du site et demande à ajouter une galerie.  L’application affiche
une liste des groupes ouverts pour la session en cours.  Le professeur choisit
un groupe, l’application affiche un champ pour entre une description de la
galerie.  Le professeur entre le nom du travail, il peut aussi inscrire
l’énoncé de ce que les étudiants doivent réaliser, ainsi que la date
d’échéance.  L’application ajoute la galerie et la rend disponible aux
étudiants. </p>

<p class=MsoNormal style='line-height:normal'>(Création d’un dossier de dépôt)</p>


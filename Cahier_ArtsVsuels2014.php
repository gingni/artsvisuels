</span></h2>

</div>

<h3>Objectif</h3>

<p class=MsoNormal style='line-height:normal'>Ajouter des images sur le serveur
local (ordinateur Mac du département des arts visuels)</p>

<h3>Description</h3>

<p class=MsoNormal style='line-height:normal'>Les étudiants déposent leurs
images (numérisations ou photos) dans les dossiers prévus à cet effet.</p>

<div style='border:none;border-bottom:solid windowtext 1.0pt;padding:0cm 0cm 1.0pt 0cm;
margin-left:14.2pt;margin-right:0cm'>

<h2 style='margin-left:0cm'><a name="_Toc381289876"><span style='font-variant:
normal !important;text-transform:uppercase'>Ajouter des images au site web</span></a></h2>

</div>

<h3>Objectif</h3>

<p class=MsoNormal style='line-height:normal'>Uploader les images sur le site
web </p>

<h3>Description</h3>

<p class=MsoNormal style='line-height:normal'>L’application crée une copie des
images du serveur local et les optimise pour le web avant de faire le transfert
sur le serveur web du Cégep.</p>

<div style='border:none;border-bottom:solid windowtext 1.0pt;padding:0cm 0cm 1.0pt 0cm;
margin-left:14.2pt;margin-right:0cm'>

<h2 style='margin-left:0cm'><a name="_Toc381289877"><span style='font-variant:
normal !important;text-transform:uppercase'>Modifier une image</span></a></h2>

</div>

<h3>Objectif</h3>

<p class=MsoNormal style='line-height:normal'>Inscrire ou modifier un titre, la
dimension, l’année de réalisation, le médium, le concept, des mots clés.</p>

<h3>Description</h3>

<p class=MsoNormal style='line-height:normal'>L’utilisateur accède à la
maintenance du site et demande à modifier une image.  L’application affiche une
fenêtre pour sélectionner l’image.  L’utilisateur peut  choisir l’image et
modifier les informations, puis cliquer sur le bouton enregistrer. 
L’application web enregistre les modifications.</p>

<div style='border:none;border-bottom:solid windowtext 1.0pt;padding:0cm 0cm 1.0pt 0cm;
margin-left:14.2pt;margin-right:0cm'>

<h2 style='margin-left:0cm'><a name="_Toc381289878"><span style='font-variant:

</div>

<h3>Objectif</h3>

<p class=MsoNormal style='line-height:normal'>Supprimer une image</p>

<h3>Description</h3>

<p class=MsoNormal style='line-height:normal'>L’utilisateur accède à la
maintenance du site et demande à supprimer une image.  L’application affiche
une fenêtre pour sélectionner l’image.  L’utilisateur peut  choisir l’image
puis cliquer sur le bouton supprimer.  L’application web supprime l’image et
affiche un message de confirmation.</p>

<div style='border:none;border-bottom:solid windowtext 1.0pt;padding:0cm 0cm 1.0pt 0cm;
